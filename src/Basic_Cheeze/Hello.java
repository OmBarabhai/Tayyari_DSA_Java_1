package Basic_Cheeze;

public class Hello {

    /*
    Printing text:

    - println prints text and moves to next line
    - \n is used for a new line inside a string

    Dry Run:
    "Hello Om"            → prints in one line
    "Hello my name is\nOm"
        → Hello my name is
          Om
    */

    public static void main(String[] args) {

        System.out.println("Hello Om");

        System.out.println(
                "Hello my name is\nOm"
        );
    }
}
