package Basic_Cheeze;

public class NonPrimitiveDataTypes {

    /*
    Non-Primitive Data Types (Basics):

    - Store multiple values or complex data
    - Created using classes
    - Can use methods

    At basic level, focus on:
    - String
    */

    public static void main(String[] args) {

        // String example
        String name = "Om";

        // String operations
        System.out.println(name);          // Om
        System.out.println(name.length()); // 2
        System.out.println(name.charAt(0));// O
    }
}
