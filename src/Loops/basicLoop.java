package Loops;

import java.util.Scanner;

public class basicLoop {
    public static void main(String[] args) {
        Scanner sc =new Scanner(System.in);
        System.out.print("Enter Number: ");
        int n = sc.nextInt();

        int i;
        for(i = 0;i <= n;i++){
            System.out.println(i +" ");
        }
//        System.out.println();
        System.out.println(i);
    }
}
