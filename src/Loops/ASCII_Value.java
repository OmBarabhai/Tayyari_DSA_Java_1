package Loops;


public class ASCII_Value {
    public static void main(String[] args) {

        for (int i = 65 ,j=97; i <= 90; i++) {


            System.out.println((char) i+"  "+ i);
            System.out.println((char) j+"  "+ j);
            j++;
            System.out.println();

        }
    }
}
